package com.simple.java.coding.array;

public class ArrayTest9 {

	public static void main(String[] args) {
		int[][] a= { {10,20},{40,50,60}};
		
		for(int i=0;i<a.length;i++) {
			
			for(int j=0;j<a[i].length;j++) {
				System.out.println(a[i][j]);
			}

				System.out.println();
			}
		}
		

	}


